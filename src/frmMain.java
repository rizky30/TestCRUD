    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if("".equals(tfNIS.getText()) || "".equals(taAlamat.getText()) ||
                "".equals(tfKelas.getText()) || "".equals(tfNama.getText()) ||
                "".equals(tfEmail.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data!", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            String JK = "";
            if(rbPa.isSelected()) {
                JK = "L";
            } else {
                JK = "P";
            }
        
            String SQL = "UPDATE t_siswa SET "
                    + "NamaSiswa ='"+tfNama.getText()+"',"+ "WHERE JenisKelamin ='"+JK+"',"
                    + "Kelas ='"+tfKelas.getText()+"',"+ "WHERE Email ='"+tfEmail.getText()+"'"
                    + "Alamat ='"+taAlamat.getText()+"'"
                    + "WHERE NIS ='"+tfNIS.getText()+"'";

            int status = KoneksiDB.execute(SQL);
            if(status == 1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Di Update!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data Gagal Di Update!", "Sukses", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void tbSiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSiswaMouseClicked
        // TODO add your handling code here:
        int baris = tbSiswa.getSelectedRow();
        if (baris != -1) {
            tfNIS.setText(tbSiswa.getValueAt(baris, 0).toString());
            tfNama.setText(tbSiswa.getValueAt(baris, 1).toString());
            if("Laki-Laki".equals(tbSiswa.getValueAt(baris, 2).toString())) {
                rbPa.setSelected(true);
            } else {
                rbPi.setSelected(true);
            }
            tfKelas.setText(tbSiswa.getValueAt(baris, 3).toString());
            taAlamat.setText(tbSiswa.getValueAt(baris, 4).toString());
            tfEmail.setText(tbSiswa.getValueAt(baris, 5).toString());
        }
    }//GEN-LAST:event_tbSiswaMouseClicked

